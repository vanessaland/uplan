/* This query is used for determining the wait times at Intersections in Waterloo, Ontario, Canada. */

SELECT 
  GeogPoint
  , PercentageStopping
  , TheDate
  , City
  , Country
  , StreetNames
  
FROM `geotab-public-intelligence.UrbanInfrastructure.IntersectionMetrics`

WHERE City LIKE 'Waterloo'
AND Country LIKE 'Canada'
